const { PrismaClient } = require("@prisma/client");
const prisma = new PrismaClient();

async function load() {
  const book = await prisma.textbook.findFirst({
    where: {
      subject: { name: "Жаратылыстану" },
      grade: 1,
      author: { contains: "Кузнецова" }
    }
  });

  if (!book) {
    console.error("❌ Ошибка: Учебник Кузнецовой не найден в базе. Пожалуйста, добавьте его в админке.");
    return;
  }

  const topics = [
    "Мен зерттеушімін",
    "Өсімдіктер — тірі ағзалар",
    "Өсімдіктердің қандай түрлері болады?",
    "Өсімдік бөліктері",
    "Өсімдіктердің тіршілігіне не қажет?",
    "Өсімдіктерге қалай күтім жасау керек?",
    "Жануарлар — тірі ағзалар",
    "Жануарлардың қандай түрлері болады?",
    "Адамдар жануарлардан не алады?",
    "Жыл мезгілдері жануарларға қалай әсер етеді?",
    "Адамның дене мүшелері",
    "Адам қалай есейеді?",
    "Қалай дұрыс тамақтану керек?",
    "Демалыс үшін не қажет?",
    "Қозғалыс дегеніміз не?",
    "Қозғалыс траекториясы дегеніміз не?",
    "Біз қалай қозғаламыз?",
    "Ғарыш дегеніміз не?",
    "Адамдар ғарышты қалай зерттеді?",
    "Қазіргі ғарышты қалай зерттейді?",
    "Жер ғаламшарының көрінісі",
    "Глобусты қалай қолданады?",
    "Уақытты анықтау не үшін қажет?",
    "Жылытқыш құралдар",
    "Электр",
    "Магнит",
    "Табиғи жарық көздері дегеніміз не?",
    "Жасанды жарық көздері дегеніміз не?",
    "Жарық не үшін қажет?",
    "Қараңғы қай кезде қажет?",
    "Табиғи дыбыс көздері дегеніміз не?",
    "Жасанды дыбыс көздері дегеніміз не?",
    "Дыбыс қалай тарайды?",
    "Дыбыс қайда тарайды?"
  ];

  console.log(`📡 Загрузка ${topics.length} тем для книги Кузнецовой (ID: ${book.id})`);

  await prisma.topic.deleteMany({ where: { textbookId: book.id } });

  for (let i = 0; i < topics.length; i++) {
    await prisma.topic.create({
      data: {
        textbookId: book.id,
        name: topics[i],
        order: i + 1
      }
    });
  }

  console.log("✅ Все темы для Кузнецовой успешно загружены!");
}

load()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
